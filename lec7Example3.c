#include <stdio.h>
int main(void){
    char i = 'b';
    switch(i){
        case 'A':
            printf(" I am in case 1\n");
            break;
        case 'B':
            printf(" I am in case 2\n");
            break;
        case 'C':
            printf(" I am in case 3\n");
            break;
        default:
            printf(" I am in default\n"); 
    }












}